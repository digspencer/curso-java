package fundamentos;

class inferencia {

	public static void main(String[] args) {
		
		double a = 4.5;
		System.out.println(a);
	
		a = 12;
		System.out.println(a);
	
		double d;    // variável foi declarada
		d = 123.13; // variável foi inicializada
		System.out.println(d); // variável usada
		
	}
}